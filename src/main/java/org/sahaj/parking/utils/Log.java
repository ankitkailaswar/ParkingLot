package org.sahaj.parking.utils;

/**
 * The type Log.
 */
public class Log {

    /**
     * Debug logs
     *
     * @param msg the msg
     */
    public static void d(String msg) {
        System.out.println(msg);
    }

    /**
     * Info logs
     *
     * @param msg the msg
     */
    public static void i(String msg) {
        System.out.println(msg);
    }

    /**
     * Warning logs
     *
     * @param msg the msg
     */
    public static void w(String msg) {
        System.out.println(msg);
    }

    /**
     * Error logs.
     *
     * @param msg the msg
     */
    public static void e(String msg) {
        System.out.println(msg);
    }
}
